import { dayWeek } from "./8.1";
// Тестирование определения дня недели по дате
describe("dayWeek", () => {
  it("Для даты 17.11.2022 должен быть четверг", () => {
    expect(dayWeek("17.11.2022")).toBe("Четверг");
  });
  it("Для даты 20.11.2022 должено быть - воскресенье", () => {
    expect(dayWeek("20.11.2022")).toBe("Воскресенье");
  });
  it("Для даты 1.01.2023 должено быть - воскресенье", () => {
    expect(dayWeek("1.01.2023")).toBe("Воскресенье");
  });
});
