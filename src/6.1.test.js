import { diff } from "./6.1";
// тестриование нахождения разницы между наибольшим и наименьшим
describe("diff", () => {
  it("разница между 5 и 3 равна 2", () => {
    expect(diff(5, 3)).toEqual(2);
  });
  it("разница между 300 и 3 равна 2", () => {
    expect(diff(300, 3)).toEqual(297);
  });
  it("разница между -4 и 3 равна 2", () => {
    expect(diff(3, -4)).toEqual(7);
  });
});
